/** Read a File into a data URL so admin uploads persist in localStorage
 *  without any backend. (Keep uploads modest — localStorage caps ~5MB.) */
export function fileToDataUrl(file: File): Promise<string> {
  return new Promise((resolve, reject) => {
    const reader = new FileReader();
    reader.onload = () => resolve(reader.result as string);
    reader.onerror = reject;
    reader.readAsDataURL(file);
  });
}

export const MAX_UPLOAD_BYTES = 4_500_000; // ~4.5MB safety margin for localStorage

export function prettyBytes(n: number) {
  if (n < 1024) return `${n} B`;
  if (n < 1024 * 1024) return `${(n / 1024).toFixed(0)} KB`;
  return `${(n / 1024 / 1024).toFixed(1)} MB`;
}
